/**
 * Variable 선언하기
 * 
 * 1) var - 더 이상 사용X
 * 2) let
 * 3) const
 */

var name = 'hello';
console.log(name);

var age = 32;
console.log(age);

let ive = '아이브';
console.log(ive);

/**
 * let / var -> 변경 가능
 */

ive = '안유진';
console.log(ive);

const newJeans = '뉴진스';
console.log(newJeans);

// newJeans = '누진스';

